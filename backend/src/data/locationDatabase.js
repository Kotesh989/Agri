// PIN Code lookup resolver
export function resolvePincode(pincode) {
  const cleanPin = String(pincode).trim();
  
  for (const taluk in LOCATIONS.villages) {
    const found = LOCATIONS.villages[taluk].find(v => v.pincode === cleanPin);
    if (found) {
      // Find district
      let parentDistrict = '';
      for (const district in LOCATIONS.taluks) {
        if (LOCATIONS.taluks[district].includes(taluk)) {
          parentDistrict = district;
          break;
        }
      }
      
      // Find state
      let parentState = '';
      for (const state in LOCATIONS.districts) {
        if (LOCATIONS.districts[state].includes(parentDistrict)) {
          parentState = state;
          break;
        }
      }

      return {
        country: 'India',
        state: parentState,
        district: parentDistrict,
        taluk: taluk,
        village: found.name,
        pincode: cleanPin
      };
    }
  }

  return null;
}
